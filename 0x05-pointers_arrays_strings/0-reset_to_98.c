#include "main.h"
/**
 *reset_to_98 - reset value of an integer to 98
 *@n: parameter targeted
 *Return : nothing
 */

/*
*1. Function reset_to_98 takes an int pointer as a parameter
2. Tis setting the value of the int pointed to by n to 98
*/
/*
*Time Complexity: O(1)
*/
void reset_to_98(int *n)
{
	*n = 98;
}
